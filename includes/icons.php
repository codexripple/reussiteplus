/**
 * Retourne un élément <svg> inline.
 *
 * @param string $name    Nom de l'icône (clé dans ICONS)
 * @param string $class   Classes CSS supplémentaires
 * @param int    $size    Taille en pixels (width & height)
 * @param array  $attrs   Attributs HTML additionnels ['aria-label'=>'...']
 */
function icon(string $name, string $class = '', int $size = 18, array $attrs = []): string
{
    $paths = ICONS[$name] ?? ICONS['help-circle'];
    $w = $h = $size;
    $cls = trim('icon ' . $class);

    $extra = '';
    foreach ($attrs as $k => $v) {
        $extra .= ' ' . htmlspecialchars($k) . '="' . htmlspecialchars($v) . '"';
    }

    return sprintf(
        '<svg xmlns="http://www.w3.org/2000/svg" width="%d" height="%d" viewBox="0 0 24 24" '
        . 'fill="none" stroke="currentColor" stroke-width="1.8" '
        . 'stroke-linecap="round" stroke-linejoin="round" class="%s"%s aria-hidden="true">%s</svg>',
        $w, $h, htmlspecialchars($cls), $extra, $paths
    );
}

/**
 * Retourne le SVG d'une icône remplie (filled).
 * Uniquement pour les icônes de style "solid" comme star, crown…
 */
function icon_solid(string $name, string $class = '', int $size = 18): string
{
    $paths = ICONS[$name] ?? ICONS['help-circle'];
    $cls = trim('icon ' . $class);
    return sprintf(
        '<svg xmlns="http://www.w3.org/2000/svg" width="%d" height="%d" viewBox="0 0 24 24" '
        . 'fill="currentColor" stroke="none" class="%s" aria-hidden="true">%s</svg>',
        $size, $size, htmlspecialchars($cls), $paths
    );
}
